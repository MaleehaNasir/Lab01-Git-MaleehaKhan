`timescale 1ns / 1ps
module addressDecoderTop(
    input         clk,
    input         rst,
    input  [31:0] address,
    input         readEnable,
    input         writeEnable,
    input  [31:0] writeData,
    input  [15:0] switches,
    output [31:0] readData,
    output [15:0] leds
);

    // ?? Address decoder ??????????????????????????????????????????????????????
    wire dec_dataMemRead, dec_dataMemWrite;
    wire dec_LEDWrite, dec_SwitchRead;

    address_decoder u_addr_dec (
        .address         (address[9:0]),
        .dataMemRead     (dec_dataMemRead),
        .dataMemWrite    (dec_dataMemWrite),
        .LEDWrite        (dec_LEDWrite),
        .SwitchReadEnable(dec_SwitchRead)
    );

    // ?? Gated enables ????????????????????????????????????????????????????????
    wire dataMem_writeEn = dec_dataMemWrite & writeEnable;
    wire led_writeEn     = dec_LEDWrite     & writeEnable;
    wire sw_readEn       = dec_SwitchRead   & readEnable;

    // ?? Per-device read data ?????????????????????????????????????????????????
    wire [31:0] dataMem_rdata;
    wire [31:0] led_rdata;
    wire [31:0] sw_rdata;

    // ?? Submodules ???????????????????????????????????????????????????????????
    data_memory_mod u_data_mem (
        .clk      (clk),
        .memWrite (dataMem_writeEn),
        .address  (address[9:0]),
        .writeData(writeData),
        .readData (dataMem_rdata)
    );

    leds u_leds (
        .clk        (clk),
        .rst        (rst),
        .btns       (16'b0),
        .writeData  (writeData),
        .writeEnable(led_writeEn),
        .readEnable (1'b0),
        .memAddress (address[31:2]),
        .switches   (switches),
        .readData   (led_rdata)
    );

    switches u_switches (
        .clk        (clk),
        .rst        (rst),
        .btns       (16'b0),
        .switches   (switches),
        .readEnable (sw_readEn),
        .writeData  (32'b0),
        .writeEnable(1'b0),
        .readData   (sw_rdata)
    );

    // ?? Read data mux ????????????????????????????????????????????????????????
    assign readData = (address[9:8] == 2'b00) ? dataMem_rdata :
                      (address[9:8] == 2'b10) ? sw_rdata      :
                                                32'b0;

    // ?? LED output ???????????????????????????????????????????????????????????
    assign leds = led_rdata[15:0];

endmodule